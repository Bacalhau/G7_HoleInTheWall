volatile unsigned int * audio_ip = (unsigned int*) XPAR_MYIP_SONG_0_S00_AXI_BASEADDR;//Base Address of the IP

int number_notes (int i)// write number of notes into IP
{
	int temp;
	temp = i << 4;
	* (audio_ip+3)= temp | 1;;  //10 notes

	return 0;
}

int set(int addr,int frequent,int duty,int time)// map frequency, duty cycle and note duration into counters
{
	int temp;
	temp = 100000000 / frequent;
    * (audio_ip)=addr<<24 | temp;
	temp = temp * duty / 100;
	* (audio_ip+1)=addr<<24 | temp;
	temp = 1000 * time / 655;
	* (audio_ip+2)=addr<<24 | temp;

	return 0;
}

